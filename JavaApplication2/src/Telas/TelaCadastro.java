    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String Nome = this.NomeField.getText();
        String Cpf = this.CPFField.getText();
        String Documento = this.DocumentoField.getText();
        String Endereco = this.EnderecoField.getText();
        String Celular = this.CelularField.getText();
        String Email = this.EmailField.getText();


        Pessoa pessoas = new Pessoa(null, Nome, Cpf, Documento, Endereco, Celular, Email);
        repositorio.pessoas.add(pessoas);

        JOptionPane.showMessageDialog(this, "Cliente Cadastrado!");
        limpar();
    }//GEN-LAST:event_jButton1ActionPerformed

